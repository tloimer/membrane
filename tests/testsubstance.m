  fprintf('T [K]\tnist\tmatlab\t100*(matlab-nist)/nist\n');
  fprintf('%3.0f\t%.4g\t%.4g\t%.3f\n', ...
    [X'; datanist'; datam'; 100*(datam./datanist-1)']);
